#!/usr/bin/python3

'''
 bars.py: this sub module of newmodules, prints bars of different characters
 it includes normal bar, plus bar, dollar bar
'''

def normal(num):
    '''
    function to print normal bar using '-' as character
    '''
    print('-'*num)

def plus(num):
    '''
    function to print plus bar using '+' as character
    '''
    print('+'*num)

def plus(num):
    '''
    function to print dollar bar using '$' as character
    '''
    print('$'*num)

